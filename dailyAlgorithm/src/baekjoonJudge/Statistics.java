package baekjoonJudge;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.*;

public class Statistics {

	/*
	  문제
		수를 처리하는 것은 통계학에서 상당히 중요한 일이다. 통계학에서 N개의 수를 대표하는 기본 통계값에는 다음과 같은 것들이 있다. 단, N은 홀수라고 가정하자.
		
		산술평균 : N개의 수들의 합을 N으로 나눈 값
		중앙값 : N개의 수들을 증가하는 순서로 나열했을 경우 그 중앙에 위치하는 값
		최빈값 : N개의 수들 중 가장 많이 나타나는 값
		범위 : N개의 수들 중 최댓값과 최솟값의 차이
		N개의 수가 주어졌을 때, 네 가지 기본 통계값을 구하는 프로그램을 작성하시오.
		
	입력
		첫째 줄에 수의 개수 N(1 ≤ N ≤ 500,000)이 주어진다. 그 다음 N개의 줄에는 정수들이 주어진다. 입력되는 정수의 절댓값은 4,000을 넘지 않는다.
		
	출력
		첫째 줄에는 산술평균을 출력한다. 소수점 이하 첫째 자리에서 반올림한 값을 출력한다.
		
		둘째 줄에는 중앙값을 출력한다.
		
		셋째 줄에는 최빈값을 출력한다. 여러 개 있을 때에는 최빈값 중 두 번째로 작은 값을 출력한다.
		
		넷째 줄에는 범위를 출력한다.
	 */
	public static void main(String[] args) {
		BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
		try {
			int X = Integer.parseInt(reader.readLine());
			int[] arr= new int[X];
			for(int i = 0; i < X; i++){
				arr[i] = Integer.parseInt(reader.readLine());
			}
			// 평균값
			System.out.println(avg(arr));
			// 중앙 값
			System.out.println(centerVal(arr));
			// 최빈 값
			System.out.println(mode(arr));
			// 범위
			System.out.println(range(arr));
		} catch (IOException e) {
			e.printStackTrace();
		}
	}
	
	private static int avg(int[] arr) {
		float sum = 0;
		for (int i = 0; i < arr.length; i++) {
			sum += arr[i];
		}
		float result = sum / arr.length;
		return Math.round(result);
	}
	
	private static int centerVal(int[] arr) {
		int[] val = arr;
		Arrays.sort(val);
		return val[arr.length/2];
	}
	
	private static int mode(int[] arr) {
		Map<Integer, Integer> map = new HashMap<>();
		if(arr.length == 1) {
			return arr[0];
		}else {
			for (int i = 0; i < arr.length; i++) {
				map.put(arr[i], map.getOrDefault(arr[i], 0) + 1);
			}
			Integer max = Collections.max(map.values());
			Map<Integer, Integer> tmpMap = new HashMap<>();
			for (Map.Entry<Integer, Integer> entry : map.entrySet()) {
				Integer value = entry.getValue();
				if(null != value && max == value) {
		            tmpMap.put(entry.getKey(), entry.getValue());
		        }
			}
			Set<Integer> set = tmpMap.keySet();
			List<Integer> result = new ArrayList(set);
			Collections.sort(result);
			if(result.size()>1) {
				return result.get(1);
			}else {
				return result.get(0);
			}
		}
	}
	
	private static int range(int[] arr) {
		int[] val = arr;
		Arrays.sort(val);
		if(val.length==1) {
			return 0;
		}else {
			return val[arr.length-1] - val[0];
		}
	}

}
